<p><strong>Adriana Rosa - Locutora.com</strong> está comprometida com o cumprimento das leis que protegem a privacidade dos dados dos clientes e do público em geral. Esta página informa como seguimos as normas de proteção de dados, quais informações coletamos durante a navegação em nossos canais de comunicação e como essas informações são utilizadas e protegidas, em conformidade com a Lei nº 13.709/2018 (Lei Geral de Proteção de Dados — LGPD). A utilização dos nossos produtos, serviços, plataformas e demais meios de interação está condicionada ao aceite desta Política de Privacidade.</p>

<h2>1. Fonte de dados pessoais</h2>
<p><strong>Adriana Rosa - Locutora.com</strong> poderá ter acesso a dados pessoais nas seguintes situações:</p>
<ul>
  <li><strong>Interação com clientes:</strong> quando o cliente fornece dados necessários à prestação dos serviços e utilização dos produtos oferecidos.</li>
  <li><strong>Interação com colaboradores:</strong> quando funcionários e colaboradores utilizam sistemas e plataformas para cumprir deveres decorrentes da relação contratual.</li>
</ul>

<h2>2. Tipos de dados pessoais coletados</h2>
<p>Sem prejuízo dos dados tratados na relação com seus colaboradores, poderão ser coletados:</p>
<ul>
  <li>Nome, e-mail, endereço, telefone, dados bancários e outros dados de contato;</li>
  <li>Informações do dispositivo, como endereço IP, localização aproximada ou provedor;</li>
  <li>Informações de uso, histórico de navegação e recursos mais utilizados.</li>
</ul>
<p>Dados pessoais sensíveis eventualmente coletados terão sua finalidade informada separadamente e dependerão do consentimento específico do usuário. Em caso de discordância, serão eliminados das bases da Adriana Rosa - Locutora.com.</p>
<p>Não coletamos intencionalmente dados de menores de idade sem autorização dos pais ou responsáveis. Caso uma coleta indevida seja identificada, os dados serão excluídos.</p>

<h2>3. Utilização dos dados pessoais</h2>
<p>Os dados coletados poderão ser utilizados para:</p>
<ul>
  <li>Fornecer e melhorar os serviços contratados;</li>
  <li>Cumprir obrigações legais ou regulatórias;</li>
  <li>Administrar o relacionamento e as solicitações dos usuários;</li>
  <li>Configurar e administrar contas;</li>
  <li>Fornecer atendimento, suporte e treinamento;</li>
  <li>Entrar em contato com os usuários;</li>
  <li>Realizar investigação, desenvolvimento, testes e aperfeiçoamento dos serviços;</li>
  <li>Enviar comunicações de marketing quando permitido por lei;</li>
  <li>Atender auditorias e obrigações de segurança da informação;</li>
  <li>Enviar confirmações, cancelamentos, atualizações e alterações relacionadas aos serviços;</li>
  <li>Proteger direitos, redes, sistemas e propriedades da Adriana Rosa - Locutora.com e de terceiros;</li>
  <li>Atender solicitações de tribunais, autoridades, órgãos normativos e instituições públicas;</li>
  <li>Exercer direitos e cumprir contratos, leis e regulamentos aplicáveis.</li>
</ul>

<h2>4. Compartilhamento com terceiros</h2>
<p>Os dados poderão ser compartilhados, quando necessário, com:</p>
<ul>
  <li>Empresas responsáveis por cadastros ou acessos utilizados pelo usuário;</li>
  <li>Prestadores de serviços e empresas parceiras que auxiliem na operação dos serviços;</li>
  <li>Órgãos governamentais, autoridades reguladoras e Poder Judiciário, quando exigido por lei;</li>
  <li>Terceiros envolvidos em venda, fusão, aquisição, reestruturação, cessão ou transferência do negócio;</li>
  <li>Terceiros necessários à proteção dos direitos da Adriana Rosa - Locutora.com;</li>
  <li>Pessoa física ou jurídica indicada pelo usuário para portabilidade de dados.</li>
</ul>

<h2>5. Transferência internacional de dados</h2>
<p>A Adriana Rosa - Locutora.com não pretende transferir dados pessoais para fora do Brasil. Caso isso se torne necessário fora das hipóteses legais, os usuários serão previamente informados e as regras da LGPD e da Autoridade Nacional de Proteção de Dados (ANPD) serão observadas.</p>

<h2>6. Proteção dos dados pessoais</h2>
<p>Utilizamos tecnologias e procedimentos administrativos, técnicos e físicos adequados ao nível de risco e ao serviço fornecido. Empregamos esforços razoáveis de mercado, provedores qualificados, certificados SSL e métodos de proteção contra acesso não autorizado.</p>
<p>Apesar dessas medidas, a natureza da internet não permite garantir proteção absoluta contra ações mal-intencionadas. Eventuais responsabilidades serão tratadas nos limites previstos em lei.</p>
<p>Incidentes de segurança que possam acarretar risco ou dano relevante serão comunicados à ANPD e aos titulares afetados, observados os prazos e requisitos legais. A comunicação poderá incluir:</p>
<ul>
  <li>A natureza dos dados afetados;</li>
  <li>Informações sobre os titulares envolvidos;</li>
  <li>Medidas técnicas e de segurança utilizadas;</li>
  <li>Riscos relacionados ao incidente;</li>
  <li>Justificativa para eventual demora na comunicação;</li>
  <li>Medidas adotadas para reverter ou mitigar os efeitos.</li>
</ul>

<h2>7. Tratamento e responsabilidade</h2>
<ul>
  <li>Quando os dados forem fornecidos por clientes para execução de serviços, a Adriana Rosa - Locutora.com atuará como Operadora, nos termos da LGPD e segundo as orientações do Controlador;</li>
  <li>Em relação aos dados de seus colaboradores, atuará como Controladora, com base na execução de contratos, cumprimento de obrigação legal ou regulatória e, quando aplicável, consentimento.</li>
</ul>
<p>Poderão ser adotadas bases legais diferentes para tratamentos distintos.</p>

<h2>8. Prazo de tratamento</h2>
<p>O período de retenção será definido considerando:</p>
<ul>
  <li>O tempo necessário para cumprir a finalidade da coleta;</li>
  <li>O encerramento da utilização dos serviços;</li>
  <li>A revogação do consentimento ou solicitação de eliminação;</li>
  <li>A necessidade de demonstrar o cumprimento de deveres e obrigações;</li>
  <li>Prazos legais, regulatórios ou judiciais;</li>
  <li>Determinações da ANPD;</li>
  <li>Cumprimento de obrigação legal ou regulatória;</li>
  <li>Execução de contratos e defesa em disputas judiciais;</li>
  <li>Transferência a terceiros nos termos da lei;</li>
  <li>Uso exclusivo do controlador, com dados anonimizados e sem acesso de terceiros.</li>
</ul>

<h2>9. Direitos do usuário</h2>
<p>Nos termos da LGPD, o usuário poderá exercer, quando aplicável:</p>
<ul>
  <li><strong>Confirmação:</strong> saber se existe tratamento de seus dados;</li>
  <li><strong>Acesso:</strong> solicitar informações e cópia dos dados tratados;</li>
  <li><strong>Correção:</strong> corrigir dados incompletos, inexatos ou desatualizados;</li>
  <li><strong>Anonimização, bloqueio ou eliminação:</strong> solicitar providências sobre dados desnecessários, excessivos ou tratados em desconformidade;</li>
  <li><strong>Exclusão:</strong> solicitar a eliminação dos dados, ressalvadas as hipóteses legais de conservação;</li>
  <li><strong>Informação:</strong> conhecer entidades públicas e privadas com as quais houve compartilhamento;</li>
  <li><strong>Recusa de marketing:</strong> cancelar o recebimento de comunicações promocionais;</li>
  <li><strong>Portabilidade:</strong> solicitar transferência dos dados, observadas as normas aplicáveis;</li>
  <li><strong>Revogação do consentimento:</strong> cancelar consentimentos anteriormente concedidos.</li>
</ul>
